package sample;

public class pair
{
    private double X;
    private double Y;

    public pair(double x, double y) { X = x; Y = y; }

    public double getX() { return X; }
    public void setX(double x) { X = x; }
    public double getY() { return Y; }
    public void setY(double y) { Y = y; }
}
